const originalString = "HELLO WORLD";
const lowerCaseString = originalString.toLowerCase();

console.log(lowerCaseString); // output: "hello world"

// En este ejemplo, el método .toLowerCase() convierte todas las letras del string originalString en minúsculas y lo guarda en la variable lowerCaseString. Luego, la función console.log() imprime lowerCaseString en la consola, lo que mostrará el string convertido a minúsculas.
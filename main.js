const originalString = "   Hello world!   ";
const trimmedStartString = originalString.trimStart();

console.log(trimmedStartString); // output: "Hello world!   "

// En este ejemplo, el método .trimStart() elimina todos los espacios en blanco al inicio del string originalString y lo guarda en la variable trimmedStartString. Luego, la función console.log() imprime trimmedStartString en la consola, lo que mostrará el string sin los espacios en blanco al inicio.
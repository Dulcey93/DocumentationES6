const mensaje = 'Hola Mundo!';
const incluyeMundo = mensaje.includes('Mundo');
console.log(incluyeMundo); // salida: true

// En este ejemplo, el método String.includes() se utiliza para verificar si una cadena de texto contiene una subcadena dada. La variable mensaje contiene una cadena de texto que dice "Hola Mundo!". Luego, el método includes() se utiliza para verificar si la cadena de texto contiene la subcadena "Mundo". El resultado de la verificación se almacena en la variable incluyeMundo, que es un valor booleano (true o false).

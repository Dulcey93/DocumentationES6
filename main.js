const mensaje = 'Hola Mundo!';
const indice = mensaje.indexOf('Mundo');
console.log(indice); // salida: 5

// En este ejemplo, el método String.indexOf() se utiliza para obtener el índice de la primera aparición de una subcadena dada en una cadena de texto. La variable mensaje contiene una cadena de texto que dice "Hola Mundo!". Luego, el método indexOf() se utiliza para obtener el índice de la subcadena "Mundo". El resultado se almacena en la variable indice.


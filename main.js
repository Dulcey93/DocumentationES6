// Suma
let a = 5;
let b = 10;
let c = a + b; // c es igual a 15
let d = "Hola ";
let e = "mundo";
let f = d + e; // f es igual a "Hola mundo"

// Resta
let g = 20;
let h = 10;
let i = g - h; // i es igual a 10

// Multiplicación
let j = 5;
let k = 10;
let l = j * k; // l es igual a 50

// División
let m = 100;
let n = 20;
let o = m / n; // o es igual a 5

// Resto
let p = 10;
let q = 3;
let r = p % q; // r es igual a 1

// Incremento
let s = 5;
s++; // s es igual a 6

// Decremento
let t = 10;
t--; // t es igual a 9

/* También es posible realizar operaciones aritméticas con asignación, utilizando los operadores +=, -=, *=, /= y %=: */

let u = 5;
u += 10; // u es igual a 15
let v = 10;
v -= 5; // v es igual a 5
let w = 3;
w *= 4; // w es igual a 12
let x = 20;
x /= 5; // x es igual a 4
let y = 10;
y %= 3; // y es igual a 1
